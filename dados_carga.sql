select T.CODPROJETO,t.CODTALHAO,a.NOMEATIVIDADE,i.CODTIPOINTERVENCAO,YEAR(S.DATAINICIO) as ANO, cast(t.AREAMANEJADA as float) as AREA
  , p.DESCPRODUTO
  , cast(m.VOLMEDIDO as float) as VOLMEDIDO , u.SIGLAUNIDADE
  , CAST(ROUND(isnull(m.VOLCORRIGIDO, case when u.SIGLAUNIDADE ='st' then m.VOLMEDIDO/1.5 else m.VOLMEDIDO end),1) as float) as CARGA
 from EVTSILVICULTURA s 
  join EVTMOVMADEIRA m on m.SEQATIVIDADE = s.SEQATIVIDADE
  join EVTATIVIDADE a on a.CODATIVIDADE = s.CODATIVIDADE 
  join AUXUNIDADE u on u.CODUNIDADE = a.CODUNIDADE
  join viewrptAREAS_check t on t.CHATALHAO = s.CHATALHAO
  join CADINTERVENCAO i on i.CODAUTORIZAINTERV = m.CODAUTORIZAINTERV
  join INVPRODUTO p on p.CODPRODUTO = m.CODPRODUTO
 where a.NOMEATIVIDADE like 'CARGA%'  and YEAR(S.DATAINICIO) =2018 
